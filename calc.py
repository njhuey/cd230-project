def add(x: int, y: int) -> int:
    return x + y


def sub(x: int, y: int) -> int:
    return x - y


def mult(x: int, y: int) -> int:
    return x * y


def div(x: int, y: int) -> int:
    return x // y


def lt(x: int, y: int) -> bool:
    return x < y


def lte(x: int, y: int) -> bool:
    return x <= y


def gt(x: int, y: int) -> bool:
    return x > y


def gte(x: int, y: int) -> bool:
    return x >= y
